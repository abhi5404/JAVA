 package Methods;

public class BuiltInFunc {
    public static void main(String[] args) {
        System.out.println(Math.sqrt(16));  // Output: 4.0
        System.out.println(Math.pow(2, 3)); // Output: 8.0
        System.out.println(Math.cbrt(100));
        System.out.println(Math.floor(-7.4));
        System.out.println(Math.ceil(7.4));
        System.out.println(Math.min(3, 9));
        System.out.println(Math.max(3, 9));
    }
}
